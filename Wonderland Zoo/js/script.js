const x = document.querySelector("image1");
const changes = document.getElementById("text1");
x.addEventListener("mouseenter", function () {
  changes.classList.add("sen");
  x.classList.add("invis");
});
x.addEventListener("mouseleave", function () {
  changes.classList.remove("sen");
  x.classList.remove("invis");
});
